import turtle

screen = turtle.Screen()
screen.bgcolor("lightblue")

t = turtle.Turtle()
t.speed(5)
t.pensize(2)

t.color("yellow")
t.begin_fill()
t.circle(100, 90)
t.circle(50, 90)
t.circle(100, 90)
t.circle(50, 90)
t.end_fill()

t.penup()
t.goto(-70, 0)
t.setheading(0)
t.pendown()
t.begin_fill()
t.circle(100, -90)
t.circle(50, -90)
t.circle(100, -90)
t.circle(50, -90)
t.end_fill()

t.penup()
t.goto(-35, -15)
t.setheading(270)
t.pendown()
t.begin_fill()
t.circle(70, 90)
t.circle(35, 90)
t.circle(70, 90)
t.circle(35, 90)
t.end_fill()

t.penup()
t.goto(-35, -15)
t.setheading(270)
t.pendown()
t.begin_fill()
t.circle(-70, 90)
t.circle(-35, 90)
t.circle(-70, 90)
t.circle(-35, 90)
t.end_fill()

t.penup()
t.goto(-40, 110)
t.color("brown")
t.pendown()
t.begin_fill()
t.setheading(270)
t.forward(150)
t.left(90)
t.forward(12)
t.left(90)
t.forward(150)
t.end_fill()


t.penup()
t.goto(-70, 60)
t.color("red")
t.pendown()
t.begin_fill()
t.circle(28)
t.end_fill()

t.penup()
t.goto(55, 60)
t.pendown()
t.begin_fill()
t.circle(28)
t.end_fill()

t.pensize(5)
t.penup()
t.goto(-35, 110) 
t.color("black")
t.pendown()
t.setheading(45)
t.forward(73)

t.penup()
t.goto(-35, 110)
t.pendown()
t.setheading(135)
t.forward(73)

t.hideturtle()
screen.exitonclick()